function buildCategoryFilterOptions<T extends FilterableStatementRow>(
  rows: T[],
  selectedSection: 'all' | StatementSection,
) {
  const scopedRows = selectedSection === 'all'
    ? rows
    : rows.filter((row) => row.section === selectedSection);

  return [...new Set(scopedRows.map((row) => row.category))].sort((left, right) =>
    left.localeCompare(right),
  );
}

function filterStatementRows<T extends FilterableStatementRow>(
  rows: T[],
  selectedSection: 'all' | StatementSection,
  selectedCategory: string,
  lineItemSearch: string,
) {
  const normalizedLineItemSearch = lineItemSearch.trim().toLowerCase();

  return rows.filter((row) => {
    if (selectedSection !== 'all' && row.section !== selectedSection) {
      return false;
    }

    if (selectedCategory !== 'all' && row.category !== selectedCategory) {
      return false;
    }

    if (!normalizedLineItemSearch) {
      return true;
    }

    return row.label.toLowerCase().includes(normalizedLineItemSearch);
  });
}

function buildVarianceExplanation(section: StatementSection, variance: number) {
  if (variance === 0) {
    return 'On target versus setup';
  }

  if (section === 'liabilities' || section === 'monthly-expenses') {
    return variance > 0
      ? 'Liability increased above setup and needs control'
      : 'Liability is lower than setup and improves net worth';
  }

  return variance > 0
    ? 'Value improved above setup and supports net worth'
    : 'Value declined below setup and weakens net worth';
}

