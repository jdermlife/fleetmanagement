      if (index === monthlyExpenseRows.length - 1) {
        accumulator[row.id] = formatPercentInput(Math.max(0, 100 - runningTotal));
        return accumulator;
      }

      const nextValue = Number(((toSafeNumber(monthlyExpenseAllocationDraft[row.id] ?? '') / currentTotal) * 100).toFixed(2));
      runningTotal += nextValue;
      accumulator[row.id] = formatPercentInput(nextValue);
      return accumulator;
